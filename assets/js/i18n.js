const defaultLanguage = "en";
const storageKey = "webcurriculum-language";

function getNestedValue(source, key) {
  return key.split(".").reduce((value, segment) => value?.[segment], source);
}

function applyLanguage(language) {
  const dictionary = translations[language] || translations[defaultLanguage];
  const toggle = document.querySelector("[data-lang-toggle]");
  const cvLink = document.querySelector("[data-cv-link]");

  document.documentElement.lang = dictionary.htmlLang;
  document.title = dictionary.meta.title;

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const value = getNestedValue(dictionary, element.dataset.i18n);

    if (value) {
      element.textContent = value;
    }
  });

  document.querySelectorAll("[data-i18n-meta]").forEach((element) => {
    const value = getNestedValue(dictionary.meta, element.dataset.i18nMeta);

    if (value) {
      element.setAttribute("content", value);
    }
  });

  if (toggle) {
    toggle.setAttribute("aria-label", dictionary.switcher.label);
    toggle.setAttribute("title", dictionary.switcher.label);
  }

  if (cvLink) {
    cvLink.setAttribute("href", dictionary.cv.href);
    cvLink.setAttribute("download", dictionary.cv.filename);
  }

  localStorage.setItem(storageKey, language);
}

function getInitialLanguage() {
  const storedLanguage = localStorage.getItem(storageKey);

  if (storedLanguage && translations[storedLanguage]) {
    return storedLanguage;
  }

  const browserLanguages = navigator.languages?.length
    ? navigator.languages
    : [navigator.language];

  if (browserLanguages.some((language) => language?.toLowerCase().startsWith("pt"))) {
    return "pt";
  }

  return defaultLanguage;
}

document.addEventListener("DOMContentLoaded", () => {
  let currentLanguage = getInitialLanguage();
  const toggle = document.querySelector("[data-lang-toggle]");

  applyLanguage(currentLanguage);

  toggle?.addEventListener("click", () => {
    currentLanguage = currentLanguage === "en" ? "pt" : "en";
    applyLanguage(currentLanguage);
  });
});
